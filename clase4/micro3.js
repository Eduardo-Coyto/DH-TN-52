// Traducir palabras
// palabras: perro, gato, puerta, ventana y mesa
let palabra = "PerrO".toLowerCase();
switch (palabra) {
    case "perro":
        console.log("Dog");
    break;
    case "gato":
        console.log("Cat");
    break;
    case "puerta":
        console.log("Door");
    break;
    case "ventana":
        console.log("Window");
    break;
    case "mesa":
        console.log("Table");
    break;
    default:
        console.log("Very dificult");
    break;
}